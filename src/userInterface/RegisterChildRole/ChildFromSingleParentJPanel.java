    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String fatherName = txtFather.getText();
        String motherName = txtMother.getText();
        String fatherAge = txtFatherAge.getText();
        String motherAge = txtMotherAge.getText();
        String reason = txtReason.getText();

//        for(SingleParentDirectory spd: organization.getChildDirectory().getSingleParentDirectory()) {
            
 //       }
        SingleParent sp = enterprise.getChildDirectory().getSingleParentDirectory().addSingleParent();
        
        sp.setFatherName(fatherName);
        sp.setMotherName(motherName);
        sp.setFatherAge(fatherAge);
        sp.setMotherAge(motherAge);
        sp.setReason(reason);
        
        JOptionPane.showMessageDialog(null, "Parent Details Added Successfully!!");
    }//GEN-LAST:event_btnSubmitActionPerformed


